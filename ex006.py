# Crie um algoritimo que leia um numero e mostre o seu dobro, triplo
# e raiz quadrada.

print('===Exercício 06===')
n = int(input('Digite um numero: '))
d = n * 2
t = n * 3
p = n ** 2
print('Voce digitou o numero {}! \n O dobro dele é {} \n O triplo dele é {} \n E a Raiz Quadrada é {}'.format(n, d, t, p))
